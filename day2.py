# Creating a List with 5 elements
my_list = [10, 20, 30, 40, 50]

# Accessing elements by index
print(my_list[0])   # Access the first element (index 0)
print(my_list[2])   # Access the third element (index 2)
print(my_list[-1])  # Access the last element (negative index -1)
print(my_list[1:4]) # Access a range of elements (index 1 to 3)
